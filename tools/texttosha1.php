    <script>
    function generateSHA1() {
      var inputText = document.getElementById("inputText").value;
      if(inputText) {
        var sha1Hash = CryptoJS.SHA1(inputText).toString();
        document.getElementById("sha1Hash").value = sha1Hash;
      } else {
        document.getElementById("sha1Hash").value = "";
      }
    }

    function clearAll() {
        document.getElementById("inputText").value = "";
        document.getElementById("sha1Hash").value = "";
    }

    function copyToClipboard() {
        var copyText = document.getElementById("sha1Hash");
        copyText.select();
        copyText.setSelectionRange(0, 99999);
        navigator.clipboard.writeText(copyText.value);
        
        var btn = document.querySelector(".btn-ghost-success");
        var originalHtml = btn.innerHTML;
        btn.innerHTML = 'Copied!';
        setTimeout(() => { btn.innerHTML = originalHtml; }, 1500);
    }
